        public SoundManager()
        {
            RAGE.Game.Audio.SetAudioFlag("LoadMPData", true);
            BindManager.Add(EKey.J, (key =>
            {
                --_currentIndex;
                if (_currentIndex < 0)
                    _currentIndex = _sounds.Count - 1;
                PlaySound();
            }));

            BindManager.Add(EKey.L, (key =>
            {
                ++_currentIndex;
                if (_currentIndex >= _sounds.Count)
                    _currentIndex = 0;
                PlaySound();
            }));
        }


        public static void PlaySound()
        {
            RAGE.Game.Audio.StopSound(_currentIndex);
            var (id, soundName, soundSetName) = _sounds[_currentIndex];
            RAGE.Chat.Output($"Playing ({id}): {soundName} - {soundSetName}");
            RAGE.Game.Audio.PlaySoundFrontend(id, soundName, soundSetName, true);

            
        }
